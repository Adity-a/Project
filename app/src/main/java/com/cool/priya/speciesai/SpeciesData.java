package com.cool.priya.speciesai;

public class SpeciesData {
    private String key;
    private String value;

    public SpeciesData(String key,String value){
        this.key = key;
        this.value = value;
    }

    public String getKey() {
        return key;
    }

    public String getValue() {
        return value;
    }
}
